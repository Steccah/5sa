-- Creazione della tabella Artisti
CREATE TABLE IF NOT EXISTS Artisti (
    ArtistaID INT PRIMARY KEY AUTO_INCREMENT,
    NomeArtista VARCHAR(255) NOT NULL
);

-- Creazione della tabella Album
CREATE TABLE IF NOT EXISTS Album (
    AlbumID INT PRIMARY KEY AUTO_INCREMENT,
    Titolo VARCHAR(255) NOT NULL,
    AnnoPubblicazione INT,
    Genere VARCHAR(50),
    ArtistaID INT,
    FOREIGN KEY (ArtistaID) REFERENCES Artisti(ArtistaID)
);

-- Creazione della tabella Tracce
CREATE TABLE IF NOT EXISTS Tracce (
    TracciaID INT PRIMARY KEY AUTO_INCREMENT,
    NomeTraccia VARCHAR(255) NOT NULL,
    Durata TIME,
    Ascolti INT,
    AlbumID INT,
    FOREIGN KEY (AlbumID) REFERENCES Album(AlbumID)
);

